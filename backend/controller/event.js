const express = require("express");
const catchAsyncErrors = require("../middleware/catchAsyncErrors");
const { upload } = require("../multer");
const Shop = require("../model/shop");
const Event = require("../model/event");
const ErrorHandler = require("../utils/ErrorHandler");
const { isSeller } = require("../middleware/auth");
const router = express.Router();
const fs = require("fs");

// create event
router.post(
  "/create-event",
  upload.array("images"),
  catchAsyncErrors(async (req, res, next) => {
    try {
      const shopId = req.body.shopId;
      const shop = await Shop.findById(shopId);
      if (!shop) {
        return next(new ErrorHandler("Shop Id is invalid!", 400));
      } else {
        const files = req.files;
        const imageUrls = files.map((file) => `${file.filename}`);

        const eventData = req.body;
        eventData.images = imageUrls;
        eventData.shop = shop;

        const product = await Event.create(eventData);

        res.status(201).json({
          success: true,
          product,
        });
      }
    } catch (error) {
      return next(new ErrorHandler(error, 400));
    }
  })
);

// edit event
router.put(
  "/edit-event/:id",
  upload.array("images"),
  catchAsyncErrors(async (req, res, next) => {
    try {
      const { id } = req.params;
      const shopId = req.body.shopId;
      const shop = await Shop.findById(shopId);
      if (!shop) {
        return next(new ErrorHandler("Shop Id is invalid!", 400));
      } else {
        const files = req.files;
        const imageUrls = files.map((file) => `${file.filename}`);

        const eventData = req.body;
        eventData.images = imageUrls;
        eventData.shop = shop;

        const { name, description, brand, category, tags, originalPrice, discountPrice, stock, start_Date, Finish_Date } = req.body;

        const editevent = await Event.findById(id);

        editevent.name = name;
        editevent.description = description;
        editevent.brand = brand;
        editevent.category = category;
        editevent.tags = tags;
        editevent.originalPrice = originalPrice;
        editevent.discountPrice = discountPrice;
        editevent.stock = stock;
        editevent.start_Date = start_Date;
        editevent.Finish_Date = Finish_Date;
        editevent.images = imageUrls;
        
        await editevent.save({ validateBeforeSave: false });

        res.status(201).json({
          success: true,
          editevent,
        });
      }
    } catch (error) {
      return next(new ErrorHandler(error, 400));
    }
  })
);

